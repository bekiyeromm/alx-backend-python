#!/usr/bin/env python3
"""
takes a float a and a float b as arguments and
returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    function to add to floating numbers
    a: number 1 with type float
    b: number 2 with type float
    Return: sum of a and b with return type
    of float.
    """
    return (a + b)
